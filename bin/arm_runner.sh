set -e

#####################################################
#  @brief cli
#  @description
#       Test, Trace, and Debug emulation
#       for bare-metal ARM64 binaries
#####################################################

# Default settings
MODE="run"
GDB_PORT="1234"
DOCKER_IMAGE="ubuntu:24.04"

usage() {
    cat << EOF
Usage: $(basename "$0") [OPTIONS] <path-to-arm64-binary> [binary args...]

A test and debugging runner for ARM64 binaries generated by Credence.

Options:
  -g, --debug        Start QEMU GDB server on port $GDB_PORT and pause execution
  -t, --trace        Trace raw Linux syscalls executed by the binary (-strace)
  -p, --port PORT    Override GDB port (default: $GDB_PORT)
  -h, --help         Show this help message

Examples:
  $(basename "$0") ./build/test_arm
  $(basename "$0") -t ./build/test_arm
  $(basename "$0") -g ./build/test_arm
EOF
    exit 1
}

# Parse options
POSITIONAL_ARGS=()
while [[ $# -gt 0 ]]; do
    case "$1" in
        -g|--debug)
            MODE="debug"
            shift
            ;;
        -t|--trace)
            MODE="trace"
            shift
            ;;
        -p|--port)
            GDB_PORT="$2"
            shift 2
            ;;
        -h|--help)
            usage
            ;;
        *)
            POSITIONAL_ARGS+=("$1")
            shift
            ;;
    esac
done

set -- "${POSITIONAL_ARGS[@]}"

if [ $# -lt 1 ]; then
    echo "Error: Missing binary path."
    usage
fi

BINARY_PATH="$1"
shift # Remove binary path from args, leaving any args to pass to the binary

if [ ! -f "$BINARY_PATH" ]; then
    echo "Error: Binary '$BINARY_PATH' not found."
    exit 1
fi

# Get absolute path and directory info for mounting into Docker
ABS_BINARY_PATH=$(realpath "$BINARY_PATH")
BINARY_DIR=$(dirname "$ABS_BINARY_PATH")
BINARY_NAME=$(basename "$ABS_BINARY_PATH")

case "$MODE" in
    "run")
        echo ">>> Running ARM64 binary: $BINARY_NAME"
        docker run --rm \
            -v "$BINARY_DIR:/workspace" \
            -w /workspace \
            --platform linux/arm64 \
            "$DOCKER_IMAGE" \
            "./$BINARY_NAME" "$@"

        EXIT_CODE=$?
        echo "==> Program exited with code: $EXIT_CODE"
        exit $EXIT_CODE
        ;;

    "trace")
        echo ">>> Running ARM64 binary with syscall tracing..."
        # We explicitly invoke qemu-aarch64 with -strace inside the container
        docker run --rm \
            -v "$BINARY_DIR:/workspace" \
            -w /workspace \
            --platform linux/arm64 \
            "$DOCKER_IMAGE" \
            qemu-aarch64 -strace "./$BINARY_NAME" "$@"
        ;;

    "debug")
        echo ">>> Starting GDB server for ARM64 binary on port $GDB_PORT..."
        echo ">>> In another terminal window, run:"
        echo ""
        echo "    gdb-multiarch \"$ABS_BINARY_PATH\" \\"
        echo "      -ex \"target remote localhost:$GDB_PORT\" \\"
        echo "      -ex \"layout asm\" \\"
        echo "      -ex \"layout regs\""
        echo ""
        echo "Waiting for GDB connection..."

        docker run --rm -it \
            -p "${GDB_PORT}:${GDB_PORT}" \
            -v "$BINARY_DIR:/workspace" \
            -w /workspace \
            --platform linux/arm64 \
            "$DOCKER_IMAGE" \
            qemu-aarch64 -g "$GDB_PORT" "./$BINARY_NAME" "$@"
        ;;
esac